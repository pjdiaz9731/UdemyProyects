public class Variables {

    public static void main(String[] args) {
     
        /*Uso del String
        Nota: Para hacer cambios solo se edita la variable en este caso Saludos*/

        String saludar = "Saludos";
        
        System.out.println(saludar);
        System.out.println(saludar);
        System.out.println(saludar);
        
        /*Uso del var
        Nota: Se puede usar en lugar de cualquier tipo de dato ejemplo: int, String */
        var despedirse = "Hasta pronto";
        System.out.println(despedirse);
        
        var numero =1;
        System.out.println(numero);
        
    }
}
